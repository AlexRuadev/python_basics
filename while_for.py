# While loops, infinite loops, we don't know when it's gonna end.
# For exemple, keep running while you are not tired. if you are tired, stop.
i = 1
while i < 6:
    print(i)
    i += 1

# For loops, we know exactly how many time we gonna loop (not an infinite loop)
for i in range(5):
    print(i)


# Casting, change an integer to a float
x = 1
print(float(x))
